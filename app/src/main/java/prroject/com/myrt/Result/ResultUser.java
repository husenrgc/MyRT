package prroject.com.myrt.Result;

public class ResultUser {

    String id_user;
    String nm_user;
    String nm_lengkap;
    String email;
    String pass;
    int no_ktp;
    int no_kk;
    String alamat;
    String grup;
    String foto;

    public String getId_user() {
        return id_user;
    }

    public void setId_user(String id_user) {
        this.id_user = id_user;
    }

    public String getNm_user() {
        return nm_user;
    }

    public void setNm_user(String nm_user) {
        this.nm_user = nm_user;
    }

    public String getNm_lengkap() {
        return nm_lengkap;
    }

    public void setNm_lengkap(String nm_lengkap) {
        this.nm_lengkap = nm_lengkap;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public int getNo_ktp() {
        return no_ktp;
    }

    public void setNo_ktp(int no_ktp) {
        this.no_ktp = no_ktp;
    }

    public int getNo_kk() {
        return no_kk;
    }

    public void setNo_kk(int no_kk) {
        this.no_kk = no_kk;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getGrup() {
        return grup;
    }

    public void setGrup(String grup) {
        this.grup = grup;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }
}
